/** =======================================================================
 * 
 * 【onEditで自動実行するGASを設定】
 * ・関数はそれぞれのファイルで作成する
 * ・ここでは呼び出しで実行する
 * 
 * =======================================================================
 **/

function onEditMain(e) {
  if (e && e.authMode && e.authMode === ScriptApp.AuthMode.LIMITED) return;
  // ------- 更新日時をアップデート ------------------------
  Updatedate(e);

  // ------- タスクをSE完了に変更した際に処理 ------------------------
  SV_CloseTask(e);
}
